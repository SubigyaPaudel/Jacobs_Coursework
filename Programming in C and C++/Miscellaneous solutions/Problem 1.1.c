#include <stdio.h>
#include <stdlib.h>

int main()
{
     double result;
     result = (3.0 + 1.0) / 5.0; /* The variable defined as a double will only accept double values. Hence, simply writing 3, 1 and 5 will not produce a double constant. They must be changed to 3.0, 5.0, and 5.0 respectively */
     printf("The value of 4/5 is %lf \n", result);
     return 0;
}
